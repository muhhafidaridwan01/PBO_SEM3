# Beberapa contoh exception umum di Python meliputi:

# TypeError: Terjadi ketika operasi yang dilakukan tidak cocok dengan tipe data yang digunakan.

# ValueError: Muncul ketika fungsi menerima argumen dengan nilai yang tidak valid.

# ZeroDivisionError: Terjadi ketika mencoba membagi angka dengan nol.

# IndexError: Terjadi ketika mencoba mengakses indeks yang berada di luar jangkauan dari sebuah struktur data, seperti sebuah list atau tuple.

# KeyError: Dijumpai ketika mencoba mengakses kunci yang tidak ada dalam kamus (dictionary).

# NameError: Muncul ketika mengacu pada nama yang tidak didefinisikan.

# FileNotFoundError: Terjadi ketika mencoba membuka file yang tidak ada.

# ImportError